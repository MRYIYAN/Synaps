import React from "react";
import "./Card.css";

//===========================================================================//
//                           COMPONENTE CARD                                 
//===========================================================================//
//                                                                            //
//  Recibe title y content para rellenar la tarjeta.                         //
//                                                                            //
//===========================================================================//
const Card = ({ title, content }) => {
    return (
        <div className="card">
            <h3 className="card-title">{title}</h3>
            <p className="card-content">{content}</p>
        </div>
    );
};

//===========================================================================//
//                             EXPORTACIÓN                                   
//===========================================================================//
export default Card;
